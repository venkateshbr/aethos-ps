def _billing_arrangement(value: Any, *, fallback_text: str) -> str:
    raw = str(value or "").strip().lower().replace(" ", "_").replace("-", "_")
    aliases = {
        "tm": "time_and_materials",
        "t&m": "time_and_materials",
        "fixed": "fixed_fee",
        "fixed_price": "fixed_fee",
        "capped_t&m": "capped_tm",
        "capped_time_and_materials": "capped_tm",
    }
    normalised = aliases.get(raw, raw)
    if normalised in _BILLING_ARRANGEMENTS:
        return normalised
    text = fallback_text.lower()
    if "cap" in text and (
        "time" in text or "t&m" in text or "hour" in text or "advisory" in text or "overrun" in text
    ):
        return "capped_tm"
    if "retainer" in text:
        return "retainer"
    if "milestone" in text:
        return "milestone"
    if "fixed" in text:
        return "fixed_fee"
    return "time_and_materials"


def _service_line(value: Any, *, fallback_text: str) -> str:
    raw = str(value or "").strip().lower()
    if raw in _SERVICE_LINES:
        return raw
    text = fallback_text.lower()
    if "tax" in text or "ct600" in text or "corporation" in text:
        return "tax"
    if "cosec" in text or "company secretarial" in text:
        return "cosec"
    if "payroll" in text:
        return "payroll"
    if "advisory" in text or "cfo" in text:
        return "advisory"
    if "account" in text or "book" in text:
        return "accounting"
    return "other"


