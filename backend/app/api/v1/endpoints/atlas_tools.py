class AtlasToolExecuteResponse(BaseModel):
    """Internal tool execution response returned to Hermes."""

    tool_name: str
    result: Any


def require_atlas_tool_auth(
    credentials: HTTPAuthorizationCredentials | None = Depends(_bearer_scheme),  # noqa: B008
) -> None:
    """Require the private Hermes-to-Aethos tool broker token."""
    expected = settings.aethos_hermes_tool_token.strip()
    if not expected:
        raise HTTPException(
            status_code=status.HTTP_503_SERVICE_UNAVAILABLE,
            detail="Atlas tool broker is not configured",
        )
    if credentials is None or not hmac.compare_digest(credentials.credentials, expected):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Invalid Atlas tool broker token",
            headers={"WWW-Authenticate": "Bearer"},
        )


@router.post("/execute", response_model=AtlasToolExecuteResponse)
async def execute_atlas_tool(
    payload: AtlasToolExecuteRequest,
    _auth: None = Depends(require_atlas_tool_auth),
    db: Client = Depends(get_service_role_client),  # noqa: B008
) -> AtlasToolExecuteResponse:
    """Execute one allowlisted Aethos tool for Hermes.

    Read tools return compact tenant-scoped read packs. Write-capable tools do
    not grant Hermes direct database mutation; they call existing Aethos policy
    and Inbox review paths.
    """
    try:
        context = verify_atlas_context_ref(
            payload.context_ref,
            required_scope=_READ_SCOPE,
        )
    except AtlasContextError as exc:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Invalid Atlas tool context",
        ) from exc

    tool = _TOOL_DISPATCH.get(payload.tool_name)
    if tool is None:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Atlas tool is not available",
        )

    result = await tool(db, context, payload.arguments)
    return AtlasToolExecuteResponse(tool_name=payload.tool_name, result=result)


