def _normalise_period_text(value: str) -> str | None:
    if _PERIOD_RE.match(value):
        return value
    if re.match(r"^\d{4}-\d{2}-\d{2}$", value):
        return value[:7]
    for fmt in ("%B %Y", "%b %Y"):
        try:
            return datetime.datetime.strptime(value, fmt).strftime("%Y-%m")
        except ValueError:
            continue
    return None


def _int_arg(value: Any, *, default: int, minimum: int, maximum: int) -> int:
    if value is None:
        return default
    if isinstance(value, bool):
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="integer argument must not be boolean",
        )
    try:
        parsed = int(value)
    except (TypeError, ValueError) as exc:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="integer argument is invalid",
        ) from exc
    if parsed < minimum or parsed > maximum:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"integer argument must be between {minimum} and {maximum}",
        )
    return parsed


