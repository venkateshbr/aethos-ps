def _quantised_money(value: Decimal) -> Decimal:
    return value.quantize(Decimal("0.01"))


def _optional_status(value: Any) -> str | None:
    status_value = _optional_string(value, "status")
    if status_value in {None, "all"}:
        return None
    if status_value not in _ENGAGEMENT_STATUSES:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="status is not supported",
        )
    return status_value


def _optional_string(value: Any, name: str) -> str | None:
    if value is None:
        return None
    if not isinstance(value, str) or not value.strip():
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} must be a non-empty string",
        )
    return value.strip()


def _required_string(value: Any, name: str) -> str:
    parsed = _optional_string(value, name)
    if parsed is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} is required",
        )
    return parsed


def _optional_period(value: Any) -> str | None:
    period = _optional_string(value, "period")
    if period is None:
        return None
    normalised = _normalise_period_text(period)
    if normalised is not None:
        return normalised
    if _PERIOD_RE.match(period) is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="period must be formatted YYYY-MM or Month YYYY",
        )
    return period


def _required_period(value: Any) -> str:
    period = _optional_period(value)
    if period is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="period is required",
        )
    return period


def _optional_date(value: Any, name: str) -> str | None:
    date_value = _optional_string(value, name)
    if date_value is None:
        return None
    try:
        datetime.date.fromisoformat(date_value)
    except ValueError as exc:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} must be formatted YYYY-MM-DD",
        ) from exc
    return date_value


def _optional_bool(value: Any, *, default: bool) -> bool | None:
    if value is None:
        return default
    if isinstance(value, bool):
        return value
    if isinstance(value, str):
        lowered = value.strip().lower()
        if lowered in {"true", "1", "yes", "y"}:
            return True
        if lowered in {"false", "0", "no", "n"}:
            return False
    raise HTTPException(
        status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
        detail="boolean argument is invalid",
    )


def _decimal_arg(value: Any, name: str) -> str:
    if value is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} is required",
        )
    try:
        parsed = Decimal(str(value))
    except (InvalidOperation, ValueError) as exc:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} must be a decimal value",
        ) from exc
    if parsed <= 0:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail=f"{name} must be greater than zero",
        )
    return str(parsed)


def _optional_money(value: Any) -> str | None:
    if value in (None, ""):
        return None
    try:
        parsed = Decimal(str(value).replace(",", ""))
    except (InvalidOperation, ValueError) as exc:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="money argument must be a decimal value",
        ) from exc
    if parsed < 0:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail="money argument must not be negative",
        )
